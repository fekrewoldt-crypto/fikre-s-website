// Supabase authentication middleware.
// Verifies the JWT sent in the Authorization header using Supabase's server‑side verification.
// If the token is valid, attaches `req.user = { id, role, verified }`.

require('dotenv').config();
const supabase = require('../db/supabase');

/**
 * Helper: generate a test token for use in test suite.
 * Returns a token that verifyToken middleware will accept.
 */
function makeTestToken() {
  return 'test-token-' + Date.now();
}

/**
 * Verify JWT token using Supabase's admin API.
 * Attaches email verification status to req.user.
 */
async function verifyToken(req, res, next) {
  const authHeader = req.headers['authorization'];
  const token = authHeader && authHeader.split(' ')[1];
  if (!token) {
    return res.status(401).json({ error: 'No token provided' });
  }
  // In test mode, accept tokens generated by the test login route (start with 'test-token')
  if (process.env.NODE_ENV === 'test') {
    if (token && token.startsWith('test-token')) {
      req.user = { id: 'test-user-' + Date.now(), role: 'student', verified: true };
      return next();
    }
    // Otherwise reject as invalid token
    return res.status(403).json({ error: 'Invalid or expired token' });
  }
  try {
    // supabase.auth.getUser validates the JWT and returns user data.
    const { data, error } = await supabase.auth.getUser(token);
    if (error) {
      return res.status(403).json({ error: 'Invalid or expired token' });
    }
    const user = data.user;

    // user.id is the Supabase UUID.
    // Role is read from the server-side user_profiles table, NOT from user_metadata.
    // user_metadata is client-writable via supabase.auth.updateUser(), so reading
    // role from there would allow privilege escalation. user_profiles.role is set
    // by the server at registration and protected by a DB trigger that blocks
    // authenticated users from updating the role column.
    let role = 'student';
    try {
      const { data: profileData, error: profileError } = await supabase
        .from('user_profiles')
        .select('role')
        .eq('id', user.id)
        .single();
      if (!profileError && profileData) {
        role = profileData.role || 'student';
      }
    } catch (err) {
      // user_profiles row may not exist for very old users — default to student
      console.warn('Role lookup from user_profiles failed, defaulting to student:', err.message);
    }

    // Check if email is verified
    const verified = user.email_confirmed_at !== null;
    req.user = {
      id: user.id,
      role: role,
      verified: verified,
      email: user.email
    };
    next();
  } catch (err) {
    console.error('Token verification error:', err);
    return res.status(500).json({ error: 'Internal server error' });
  }
}

/**
 * Verify email is confirmed before allowing access.
 * Use this after verifyToken for routes requiring verified email.
 */
function requireVerifiedEmail(req, res, next) {
  if (!req.user) {
    return res.status(401).json({ error: 'Not authenticated' });
  }
  if (req.user.verified === false) {
    return res.status(403).json({
      error: 'Email not verified. Please check your inbox and verify your email address.',
      code: 'EMAIL_NOT_VERIFIED'
    });
  }
  next();
}

/**
 * Helper to require one of a set of roles.
 */
function requireRole(...allowedRoles) {
  return (req, res, next) => {
    if (!req.user) {
      return res.status(401).json({ error: 'Not authenticated' });
    }
    if (!allowedRoles.includes(req.user.role)) {
      return res.status(403).json({ error: 'Insufficient permissions' });
    }
    next();
  };
}

module.exports = { verifyToken, requireRole, requireVerifiedEmail, makeTestToken };